# GPIO simulation
# SimPin objects are appended here in order as machine.Pin() is called.
# Keys a/b/c switch modes exclusively (only one low at a time); ESC = all high.
# Key d independently toggles the detail pin (index 3) without affecting mode pins.
# The detail pin starts LOW (summary mode, matching hardware default) and persists
# across soft-resets via _detail_low.
_sim_pins    = []
_desired_mode = 0      # persists across sim reboots; updated by _set_gpio_mode
_detail_low   = True   # True = pin 3 LOW = summary (default); False = HIGH = full news
_adc_value    = 32768  # simulated ADC raw value 0-65535; o=raise, p=lower (step 2048)

class _SimPin:
    __slots__ = ('_low',)
    def __init__(self):    self._low = False
    def value(self):       return 0 if self._low else 1

_KEY_TO_PIN = {
    pygame.K_a: 0,
    pygame.K_b: 1,
    pygame.K_c: 2,
}

def _set_gpio_mode(idx):
    """Pull mode pin idx low (pins 0-2 only, exclusive). idx=-1 = all mode pins high."""
    global _desired_mode
    _desired_mode = idx
    for i, p in enumerate(_sim_pins[:3]):
        p._low = (i == idx)
    _update_caption()

def _toggle_detail_pin():
    """Toggle pin 3 (detail/summary switch) independently of mode pins.
    LOW = summary (default), HIGH = full news."""
    global _detail_low
    _detail_low = not _detail_low
    if len(_sim_pins) > 3:
        _sim_pins[3]._low = _detail_low
    _update_caption()

def _update_caption():
    if not pygame.get_init():
        return
    font_tag = "C64 font" if _FONT_NAME == 'c64' else "Spectrum font"
    keys = "abc"
    n_mode = min(len(_sim_pins), 3)
    active = next((i for i, p in enumerate(_sim_pins[:3]) if p._low), None)
    adc_disp = f"  o/p=ADC:{_adc_value >> 8}"
    if _sim_pins:
        hints = "  ".join(f"{keys[i]}=mode {i}" for i in range(n_mode))
        if len(_sim_pins) > 3:
            det = "summary" if _sim_pins[3]._low else "full news"
            hints += f"  d={det}"
        mode  = f"mode {active}" if active is not None else "default"
        caption = f"pico-crt-clock sim [{font_tag}]  |  {hints}  ESC=default  [{mode}]{adc_disp}"
    else:
        caption = f"pico-crt-clock sim [{font_tag}]{adc_disp}"
    pygame.display.set_caption(caption)

def _ensure_init():
    global _screen, _surface
    if _screen is not None:
        return
    pygame.init()
    _screen  = pygame.display.set_mode(
        (WIDTH * SCALE + BORDER * 2, HEIGHT * SCALE + BORDER * 2))
    _update_caption()
    _surface = pygame.Surface((WIDTH, HEIGHT))
    _surface.fill(PALETTE[0])
    _present()

_key_queue = []

def get_key():
    """Return next pending key code or None. Simulator only - not present on hardware."""
    return _key_queue.pop(0) if _key_queue else None

def _pump():
    if not pygame.get_init():
        return
    for ev in pygame.event.get():
        if ev.type == pygame.QUIT:
            pygame.quit()
            sys.exit(0)
        elif ev.type == pygame.KEYDOWN:
            if ev.key in _KEY_TO_PIN:
                _set_gpio_mode(_KEY_TO_PIN[ev.key])
            elif ev.key == pygame.K_d:
                _toggle_detail_pin()
            elif ev.key == pygame.K_o:
                global _adc_value
                _adc_value = min(65535, _adc_value + 2048)
                _update_caption()
            elif ev.key == pygame.K_p:
                _adc_value = max(0, _adc_value - 2048)
                _update_caption()
            elif ev.key == pygame.K_ESCAPE:
                _set_gpio_mode(-1)
                global _detail_low
                _detail_low = True
                if len(_sim_pins) > 3:
                    _sim_pins[3]._low = True
                _update_caption()
            else:
                _key_queue.append(ev.key)

def _present():
    _pump()
    scaled = pygame.transform.scale(_surface, (WIDTH * SCALE, HEIGHT * SCALE))
    _screen.fill(PALETTE[_border_colour])
    _screen.blit(scaled, (BORDER, BORDER))
    pygame.display.update()

def _draw_char(x, y, ch, bg, fg, scale=1):
    """Render one glyph at pixel (x,y) on _surface using palette indices."""
    code = ord(ch) if isinstance(ch, str) else ch
    code = max(32, min(127, code))
    off  = (code - 32) * 8
    bg_c = PALETTE[bg & 0xF]
    fg_c = PALETTE[fg & 0xF]
    s = scale
    for row in range(8):
        bits = _CHARSET[off + row]
        for col in range(8):
            colour = fg_c if (bits & (0x80 >> col)) else bg_c
            px, py = x + col * s, y + row * s
            if 0 <= px < WIDTH and 0 <= py < HEIGHT:
                if s == 1:
                    _surface.set_at((px, py), colour)
                else:
                    pygame.draw.rect(_surface, colour,
                                     pygame.Rect(px, py, s, s))

