# make_icons.py
#
# Current icons converted from icons.py into editable ASCII art.
# Edit the *_rows arrays, then run this script to regenerate icons.py.

WIDTH = 32
HEIGHT = 16

CHAR_TO_VALUE = {
    ".": 0,
    "4": 4,
    "6": 6,
    "7": 7,
    "8": 8,
    "9": 9,
    "a": 10,
    "b": 11,
    "c": 12,
    "d": 13,
    "#": 15,
}

def icon_from_ascii(rows):
    if len(rows) != HEIGHT:
        raise ValueError(f"Expected {HEIGHT} rows, got {len(rows)}")
    out = bytearray()
    for y, row in enumerate(rows):
        if len(row) != WIDTH:
            raise ValueError(f"Row {y} length {len(row)} != {WIDTH}")
        for ch in row:
            try:
                out.append(CHAR_TO_VALUE[ch])
            except KeyError:
                raise ValueError(f"Unknown character {ch!r} at row {y}")
    return out

def bytes_literal(data, wrap=16):
    parts = []
    for i in range(0, len(data), wrap):
        chunk = data[i:i+wrap]
        parts.append("    b'" + "".join(f"\\x{v:02x}" for v in chunk) + "'")
    return "(\n" + "\n".join(parts) + "\n)"

def write_icons_py(filename="icons.py"):
    icons = {
        "sky_sun": icon_from_ascii(sky_sun_rows),
        "sky_partly": icon_from_ascii(sky_partly_rows),
        "sky_cloud": icon_from_ascii(sky_cloud_rows),
        "precip_rain": icon_from_ascii(precip_rain_rows),
        "precip_snow": icon_from_ascii(precip_snow_rows),
        "precip_drizzle": icon_from_ascii(precip_drizzle_rows),
        "precip_thunder": icon_from_ascii(precip_thunder_rows),
        "precip_fog": icon_from_ascii(precip_fog_rows),
    }

    with open(filename, "w", encoding="utf-8") as f:
        f.write("# This file is generated by make_icons.py; do not edit directly.\n\n")
        for name, data in icons.items():
            f.write(f"{name} = {bytes_literal(data)}\n\n")

